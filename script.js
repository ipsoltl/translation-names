function translationFunction() {
    var russianNams = document.getElementById("russianNames").value;
    var arrRus = russianNams.replace(/<.*?>/gi, '').split(/\s*\r?\n\s*|\s*,\s*/);
    
    var name = '';
    var allNames = 0;//количество имен

    for (var i = 0; i < arrRus.length; i++) {
    var out = [];

    if (arrRus[i]) {
        
        allNames++;

        if (allNames > 15) {
            break;
        }

        var n = arrRus[i].toLocaleLowerCase();
        console.log(n);
        
        var n = n.trim().split(/\s+/);
        
        if (n.length > 0) {

            if (translationOfNames[n[0]]) {
            out.push(translationOfNames[n.shift()]);

                if (transferOfTitles[n.join(' ')]) {
                    out.push('<i>' + transferOfTitles[n.join(' ')] + '</i>');
                }
            }
            else if (transferOfTitles[n[0]]){
                if (translationOfNames[n[1]] && n.length == 2) {
                    out.push('<i>' + transferOfTitles[n.shift()] + '</i>');
                    out.push(translationOfNames[n.shift()]);
                }
            }
        }
    }
    name += '<p>' + out.join(' ') + '</p>';
    document.getElementById("greekNames").innerHTML = name;
    }
};

